/**
 * Returns the translated string for the given key and language.
 * Falls back to English if the key is missing in the requested language.
 * @param {string} key  Dot-notation key, e.g. "popup.addButton"
 * @param {string} lang "en" or "de"
 * @returns {string}
 */
export function t(key, lang = "en") {
  const l = lang === "de" ? "de" : "en";
  return translations[l][key] ?? translations.en[key] ?? key;
}
